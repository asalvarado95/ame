<h4>&iquest;Qui&eacute;n fue el Br. <br /> Trinidad Figueira?</h4><br>
<center><img class="eponimo" src="http://localhost/ame/vista/images/eponimo.png"/></center>
<i style="text-align=:right;">Ep&oacute;nimo</i><br>
<br />
<p> &nbsp;  Naci&oacute; en Albarico, de humilde origen, pero dotado de una ejemplar voluntad de superaci&oacute;n y de una generosa dedicaci&oacute;n al servicio de la comunidad. Fue un educador insigne. En el a&ntilde;o de 1897 fund&oacute; en Campo El&iacute;as el colegio &quot;Rivero&quot; que funcion&oacute; en Chivacoa los a&ntilde;os 1898, 1899 y 1890.</p><br>
<p> &nbsp;  Es considerado como el <b>hombre de mayor proyecci&oacute;n dentro de los educadores de Yaracuy</b>, especialmente por su Colegio &quot;Montesinos&quot; de San Felipe, creado el 19 de diciembre de 1916 y sostenido hasta 1924, tras graduar promociones de bachilleres que, al culminar luego la etapa universitaria, brillaron con luz propia en diferentes campos del saber.</p><br>


<h4><img class="distintivo" src="http://localhost/ame/vista/images/logo_color.png"/> &nbsp; &iquest;Qu&eacute; es AME?</h4><br>

<p>&nbsp; La Aplicaci&oacute;n de Matriculaci&oacute;n Estudiantil (AME) es un sistema web <b>para la automatizaci&oacute;n de los procesos concernientes a la matr&iacute;cula discriminada</b> de los estudiantes del nivel Media General de la Unidad Educativa Trinidad Figueira.</p><br>
<p> &nbsp; En tal sentido, a trav&eacute;s de AME <b>puedes visualizar la informaci&oacute;n de los estudiantes de forma r&aacute;pida y eficiente,</b> disminuyendo el tiempo de b&uacute;squeda de datos de un estudiante en espec&iacute;fico a trav&eacute;s de los expedientes</p><br>
<p> &nbsp; Asimismo, a pesar de ser necesario tener los expedientes de manera f&iacute;sica, "AME" coadyuvar&aacute; en a crear, consultar, actualizar y procesar los datos alusivos a los estudiantes del nivel Media General en la <b>Coordinaci&oacute;n de Evaluaci&oacute;n, Registro y Control</b> de la Unidad Educativa Trinidad Figueira.</p><br>

